package com.cancer.cancerbuddy.Record_Sys;

public class GraphChatModel {

    int xValue,yValue;

    public GraphChatModel() {
    }

    public GraphChatModel(int xValue, int yValue) {
        this.xValue = xValue;
        this.yValue = yValue;
    }

    public int getxValue() {
        return xValue;
    }

    public void setxValue(int xValue) {
        this.xValue = xValue;
    }

    public int getyValue() {
        return yValue;
    }

    public void setyValue(int yValue) {
        this.yValue = yValue;
    }
}
